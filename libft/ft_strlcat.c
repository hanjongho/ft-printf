#include "libft.h"

size_t	ft_strlcat(char *dest, char *src, size_t size)
{
	size_t i;
	size_t j;

	i = 0;
	j = 0;
	while (dest[i] != '\0')
		i++;
	while (src[j] && i + j + 1 < size)
	{
		dest[i + j] = src[j];
		j++;
	}
	if (size != 0)
		dest[i + j] = '\0';
	while (src[j])
		j++;
	if (i > size)
		return (size + j);
	return (i + j);
}
